-- 안전한 테이블 삭제 (존재하는 경우만)
BEGIN
    FOR t IN (SELECT table_name FROM user_tables 
              WHERE table_name IN ('PAYMENTS', 'PAY', 'ORDER_ITEMS', 'ORDERS', 
                                   'COMMENT_LIKE', 'POST_LIKE', 'CART', 
                                   'CHAT_MESSAGE_FILES', 'CHAT_MESSAGES', 
                                   'DIRECT_CHAT_ROOMS', 'FOLLOWS', 'POST_SAVE', 
                                   'POST_COMMENT', 'POST', 'ADMIN_ACCOUNT', 
                                   'COMPANY', 'USERS'))
    LOOP
        EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
    END LOOP;
END;
/

