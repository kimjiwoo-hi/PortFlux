------------------------------------------------------------
-- 12) ORDERS 테이블 (주문 내역 관리)
------------------------------------------------------------
CREATE TABLE ORDERS (
    id NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    user_id NUMBER(20) NOT NULL,
    merchant_uid VARCHAR2(255) NOT NULL,
    total_amount NUMBER(19,2) NOT NULL,
    status VARCHAR2(50) NOT NULL,
    imp_uid VARCHAR2(255),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_orders_merchant_uid UNIQUE (merchant_uid),
    CONSTRAINT fk_orders_user FOREIGN KEY (user_id) REFERENCES USERS(user_num)
);

CREATE INDEX idx_orders_merchant_uid ON ORDERS(merchant_uid);
CREATE INDEX idx_orders_user_id ON ORDERS(user_id);
CREATE INDEX idx_orders_created_at ON ORDERS(created_at);

COMMENT ON TABLE ORDERS IS '주문 내역 관리 테이블';
COMMENT ON COLUMN ORDERS.merchant_uid IS '주문 고유 식별자 (UUID 기반)';
COMMENT ON COLUMN ORDERS.status IS '주문 상태 (CREATED, PENDING, PAID, CANCELLED)';

------------------------------------------------------------
-- 13) ORDER_ITEMS 테이블 (주문 상품 상세)
------------------------------------------------------------
CREATE TABLE ORDER_ITEMS (
    id NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    order_id NUMBER NOT NULL,
    product_id NUMBER(20) NOT NULL,
    product_name VARCHAR2(500) NOT NULL,
    unit_price NUMBER(19,2) NOT NULL,
    qty NUMBER NOT NULL,
    CONSTRAINT fk_order_items_order FOREIGN KEY (order_id) REFERENCES ORDERS(id) ON DELETE CASCADE,
    CONSTRAINT fk_order_items_product FOREIGN KEY (product_id) REFERENCES POST(post_id)
);

CREATE INDEX idx_order_items_order_id ON ORDER_ITEMS(order_id);
CREATE INDEX idx_order_items_product_id ON ORDER_ITEMS(product_id);

COMMENT ON TABLE ORDER_ITEMS IS '주문 상품 상세 정보';
COMMENT ON COLUMN ORDER_ITEMS.qty IS '주문 수량';

------------------------------------------------------------
-- 14) POST_LIKE 테이블 (게시글 좋아요)
------------------------------------------------------------
CREATE TABLE POST_LIKE (
    like_id NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    post_id NUMBER(20) NOT NULL,
    user_num NUMBER(20),
    company_num NUMBER(20),
    liked_at DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT uq_post_like_unified UNIQUE (post_id, user_num, company_num),
    CONSTRAINT ck_like_author CHECK (user_num IS NOT NULL OR company_num IS NOT NULL),
    CONSTRAINT fk_postlike_user FOREIGN KEY (user_num) REFERENCES USERS(user_num) ON DELETE SET NULL,
    CONSTRAINT fk_postlike_post FOREIGN KEY (post_id) REFERENCES POST(post_id) ON DELETE CASCADE,
    CONSTRAINT fk_postlike_company FOREIGN KEY (company_num) REFERENCES COMPANY(company_num) ON DELETE SET NULL
);

------------------------------------------------------------
-- 15) COMMENT_LIKE 테이블 (댓글 좋아요)
------------------------------------------------------------
CREATE TABLE COMMENT_LIKE (
    like_id NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    user_num NUMBER(20) NOT NULL,
    comment_id NUMBER(20) NOT NULL,
    liked_at DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT uq_comment_like UNIQUE (user_num, comment_id),
    CONSTRAINT fk_commentlike_user FOREIGN KEY (user_num) REFERENCES USERS(user_num) ON DELETE CASCADE,
    CONSTRAINT fk_commentlike_comment FOREIGN KEY (comment_id) REFERENCES POST_COMMENT(comment_id) ON DELETE CASCADE
);

------------------------------------------------------------
-- 16) PAYMENTS 테이블 (결제 기록 - 아임포트 연동)
------------------------------------------------------------
CREATE TABLE PAYMENTS (
    id NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    order_id NUMBER NOT NULL,
    imp_uid VARCHAR2(255) NOT NULL,
    merchant_uid VARCHAR2(255) NOT NULL,
    amount NUMBER(19,2) NOT NULL,
    status VARCHAR2(50) NOT NULL,
    paid_at TIMESTAMP,
    raw_response CLOB,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_payments_imp_uid UNIQUE (imp_uid),
    CONSTRAINT uq_payments_merchant_uid UNIQUE (merchant_uid),
    CONSTRAINT fk_payments_order FOREIGN KEY (order_id) REFERENCES ORDERS(id) ON DELETE CASCADE
);

CREATE INDEX idx_payments_order_id ON PAYMENTS(order_id);
CREATE INDEX idx_payments_merchant_uid ON PAYMENTS(merchant_uid);
CREATE INDEX idx_payments_imp_uid ON PAYMENTS(imp_uid);
CREATE INDEX idx_payments_status ON PAYMENTS(status);

COMMENT ON TABLE PAYMENTS IS '결제 기록 관리 테이블 (아임포트 연동)';
COMMENT ON COLUMN PAYMENTS.imp_uid IS '아임포트 거래 고유 ID';
COMMENT ON COLUMN PAYMENTS.merchant_uid IS '가맹점 주문 번호 (ORDERS.merchant_uid)';
COMMENT ON COLUMN PAYMENTS.status IS '결제 상태 (READY, PAID, CANCELLED, REFUNDED)';
COMMENT ON COLUMN PAYMENTS.raw_response IS '아임포트 응답 원본 데이터 (JSON)';

------------------------------------------------------------
-- 17) PAY 테이블 (기존 결제 테이블 유지)
------------------------------------------------------------
CREATE TABLE PAY (
    order_id NUMBER(20) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_num NUMBER(20) NOT NULL,
    pay_date DATE DEFAULT SYSDATE,
    pay_price NUMBER(20) NOT NULL,
    setle_sttus NUMBER(1) NOT NULL,
    order_name VARCHAR2(200) NOT NULL,
    cart_id NUMBER(20) NOT NULL,
    CONSTRAINT fk_pay_user FOREIGN KEY (user_num) REFERENCES USERS(user_num) ON DELETE CASCADE,
    CONSTRAINT fk_pay_cart FOREIGN KEY (cart_id) REFERENCES CART(cart_id) ON DELETE CASCADE
);

COMMIT;

